/*
Debemos lograr tomar un nombre con 'prompt' 
y luego mostrarlo por 'alert' al presionar el botón  'mostrar'*/
function mostrar()
{
	var nombrePersona; //se puede usar var o let para declarar variables.
	nombrePersona = prompt(" ¿Cuál es su nombre ?"); //toma un valor y lo tira en la variable nombre.  
	alert(nombrePersona);
}

